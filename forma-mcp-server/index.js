async function toolApplyDbChange({ slug, sql, session_note, changes_made = [], client_fields }, env) {
  const client = await getClientCreds(slug, env);
  if (!client.supabase_service_key) return { ok: false, error: 'Supabase not connected for this client' };

  const normalised = sql.trim().toLowerCase();
  if (normalised.startsWith('select')) {
    return { ok: false, error: 'Use client_query for SELECT. apply_db_change is for mutations only.' };
  }

  // 1. Run the SQL mutation against client Supabase
  const sqlRes = await fetch(`${client.supabase_url}/rest/v1/rpc/exec_sql`, {
    method:  'POST',
    headers: { 'apikey': client.supabase_service_key, 'Content-Type': 'application/json' },
    body:    JSON.stringify({ query: sql }),
  });
  if (!sqlRes.ok) {
    const err = await sqlRes.text();
    return { ok: false, step: 'client_db', error: err };
  }

  const results = [];
  results.push({ step: 'client_db', ok: true });

  // 2. Optionally update platform client record
  if (client_fields && Object.keys(client_fields).length) {
    const BLOCKED = ['github_token_enc','cloudflare_token_enc','supabase_mgmt_token_enc','supabase_service_key_enc','supabase_anon_key_enc','printify_key_enc'];
    const safe = Object.fromEntries(Object.entries(client_fields).filter(([k]) => !BLOCKED.includes(k)));
    if (Object.keys(safe).length) {
      const updateRes = await platformSupabase(env, 'PATCH', `/rest/v1/clients?slug=eq.${enc(slug)}`, safe);
      results.push({ step: 'platform_update', ok: updateRes.ok, status: updateRes.status });
    }
  }

  // 3. Optionally write session note
  if (session_note) {
    const clientIdRes = await platformSupabase(env, 'GET', `/rest/v1/clients?slug=eq.${enc(slug)}&select=id&limit=1`);
    if (clientIdRes.ok) {
      const rows = await clientIdRes.json();
      if (rows.length) {
        const row = {
          client_id:    rows[0].id,
          client_slug:  slug,
          session_date: new Date().toISOString().split('T')[0],
          summary:      session_note,
          changes_made: changes_made,
          deploy_triggered: false,
          signal_count: 0,
          style_signal_count: 0,
        };
        const sessionRes = await platformSupabase(env, 'POST', '/rest/v1/sessions_index', row, { Prefer: 'return=representation' });
        results.push({ step: 'session_note', ok: sessionRes.ok });
      }
    }
  }

  return { ok: true, steps: results };
}


function resolveMcpWorkspace(request) {
  try {
    const url = new URL(request.url);
    const raw = (url.searchParams.get('workspace') || request.headers.get('X-Formaut-Workspace') || 'core').toLowerCase();
    const aliases = { emails: 'email', mail: 'email', site: 'site', files: 'site', memory: 'memory', operations: 'operations', ops: 'operations', full: 'full', core: 'core' };
    return aliases[raw] || 'core';
  } catch {
    return 'core';
  }
}

function getToolDefinitionsForAuth(auth, workspace) {
  if (auth.role === 'operator') {
    const allTools = [...TOOL_DEFINITIONS, ...EMAIL_TOOL_DEFINITIONS];
    const manifest = getWorkspaceManifest({ workspace, operator: true });
    if (!manifest.length || manifest.includes('*')) return allTools;
    const coreAlways = new Set(['get_client', 'get_session_history', 'write_session_summary']);
    const allowed = new Set([...manifest, ...coreAlways]);
    return allTools.filter(t => allowed.has(t.name));
  }
  if (auth.role === 'client') return getClientToolDefinitionsForWorkspace(workspace);
  return [];
}

function getClientToolDefinitionsForWorkspace(workspace = 'core') {
  const core = CLIENT_TOOL_DEFINITIONS.filter(t => ['who_am_i', 'get_my_context', 'save_session_note'].includes(t.name));
  const site = CLIENT_TOOL_DEFINITIONS.filter(t => ['who_am_i', 'get_my_context', 'list_my_files', 'read_my_file', 'bulk_read_my_files', 'edit_my_file', 'deploy_my_change', 'deploy_my_site', 'check_my_deploy', 'save_session_note'].includes(t.name));
  const db = CLIENT_TOOL_DEFINITIONS.filter(t => ['who_am_i', 'get_my_context', 'query_my_db', 'update_my_db', 'save_session_note'].includes(t.name));
  if (workspace === 'email') return [...core, ...EMAIL_TOOL_DEFINITIONS.map(stripOperatorSlugFromToolSchema)];
  if (workspace === 'site') return site;
  if (workspace === 'memory') return db;
  if (workspace === 'full') return [...CLIENT_TOOL_DEFINITIONS, ...EMAIL_TOOL_DEFINITIONS.map(stripOperatorSlugFromToolSchema)];
  return core;
}

function stripOperatorSlugFromToolSchema(tool) {
  const clone = JSON.parse(JSON.stringify(tool));
  if (clone.inputSchema?.properties?.slug) delete clone.inputSchema.properties.slug;
  if (Array.isArray(clone.inputSchema?.required)) clone.inputSchema.required = clone.inputSchema.required.filter(k => k !== 'slug');
  return clone;
}


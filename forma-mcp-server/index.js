async function toolBulkFileRead({ slug, paths }, env) {
  if (!paths || !paths.length) return { ok: false, error: 'paths array is required' };
  if (paths.length > 5) return { ok: false, error: 'Maximum 5 files per bulk_file_read call' };

  const client = await getClientCreds(slug, env);
  if (!client.github_token) return { ok: false, error: 'GitHub not connected for this client' };

  // Fetch all files in parallel
  const fetches = paths.map(path =>
    fetch(
      `https://api.github.com/repos/${client.github_repo}/contents/${path}`,
      { headers: githubHeaders(client.github_token) }
    ).then(async res => {
      if (res.status === 404) return { path, ok: false, error: 'not found' };
      if (!res.ok) return { path, ok: false, error: `GitHub error ${res.status}` };
      const data = await res.json();
      const content = atob(data.content.replace(/\n/g, ''));
      return { path, ok: true, content, sha: data.sha, size: data.size };
    }).catch(err => ({ path, ok: false, error: err.message }))
  );

  const results = await Promise.all(fetches);

  // Build a clean map for easy consumption
  const files = {};
  const errors = [];
  for (const r of results) {
    if (r.ok) {
      files[r.path] = { content: r.content, sha: r.sha, size: r.size };
    } else {
      errors.push({ path: r.path, error: r.error });
    }
  }

  return {
    ok:     true,
    repo:   client.github_repo,
    files,
    errors: errors.length ? errors : undefined,
    note:   `Read ${Object.keys(files).length}/${paths.length} files successfully.`,
  };
}


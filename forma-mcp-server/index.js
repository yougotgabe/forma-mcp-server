export default {
  async fetch(request, env) {
    const url = new URL(request.url);

    // CORS preflight — claude.ai needs this
    if (request.method === 'OPTIONS') {
      return new Response(null, {
        status: 204,
        headers: corsHeaders(),
      });
    }

    // Health check — public, no auth
    if (url.pathname === '/' || url.pathname === '/health') {
      return jsonResponse({ ok: true, service: 'forma-mcp-server', version: '2.0.0' });
    }

    // MCP protocol endpoint — pass request through; auth is resolved inside
    if (url.pathname === '/mcp' && request.method === 'POST') {
      return handleMCP(request, env);
    }

    // Token provisioning endpoint — operator only, for generating client tokens
    // POST /provision-client-token { slug }  → { token, instructions }
    if (url.pathname === '/provision-client-token' && request.method === 'POST') {
      return handleProvisionClientToken(request, env);
    }

    return jsonResponse({ error: 'Not found' }, 404);
  }
};

// =============================================================================
// AUTH
// =============================================================================
// Two token types share the same Bearer header mechanism:
//
//   OPERATOR  — MCP_API_KEY wrangler secret. Full access to all tools.
//   CLIENT    — per-client token generated by /provision-client-token.
//               SHA-256 hash stored in clients.mcp_token_hash.
//               Access limited to CLIENT_TOOLS only, auto-scoped to their slug.
//
// initialize and tools/list are unauthenticated so claude.ai can complete its
// handshake before the user has entered their token. The URL is unguessable.
// All tools/call requests are authenticated.

async function resolveAuth(request, env) {
  const authHeader = request.headers.get('Authorization') || '';
  const token = authHeader.startsWith('Bearer ') ? authHeader.slice(7).trim() : '';

  if (!token) return { role: 'none' };

  // Check operator token first (fast string compare)
  if (env.MCP_API_KEY && token === env.MCP_API_KEY) {
    return { role: 'operator' };
  }

  // Check client token — hash and look up in DB
  try {
    const hash = await sha256hex(token);
    const res = await platformSupabase(env, 'GET',
      `/rest/v1/clients?mcp_token_hash=eq.${enc(hash)}&select=slug,display_name,status,mcp_token_hash,tier&limit=1`
    );
    if (res.ok) {
      const rows = await res.json();
      if (rows.length) {
        return { role: 'client', slug: rows[0].slug, display_name: rows[0].display_name, tier: rows[0].tier || 'standard' };
      }
    }
  } catch {}

  return { role: 'none' };
}

async function sha256hex(input) {
  const buf = await crypto.subtle.digest('SHA-256', new TextEncoder().encode(input));
  return Array.from(new Uint8Array(buf)).map(b => b.toString(16).padStart(2, '0')).join('');
}

// =============================================================================
// MCP PROTOCOL HANDLER
// =============================================================================

async function handleMCP(request, env) {
  // Resolve auth once per request — passed into tool handlers
  const auth = await resolveAuth(request, env);

  let body;
  try {
    body = await request.json();
  } catch {
    return jsonResponse({ jsonrpc: '2.0', error: { code: -32700, message: 'Parse error' }, id: null });
  }

  const { jsonrpc, method, params, id } = body;

  if (jsonrpc !== '2.0') {
    return jsonResponse({ jsonrpc: '2.0', error: { code: -32600, message: 'Invalid Request' }, id });
  }

  try {
    switch (method) {
      case 'initialize':
        return jsonResponse(mcpResult(id, {
          protocolVersion: '2024-11-05',
          capabilities: { tools: {} },
          serverInfo: { name: 'forma-mcp-server', version: '2.0.0' },
        }));

      case 'tools/list': {
        // Return a capability/workspace-scoped tool manifest. This keeps MCP token
        // overhead low for client agents by exposing only the tools relevant to the
        // current workspace, e.g. /mcp?workspace=email.
        const workspace = resolveMcpWorkspace(request);
        const tools = getToolDefinitionsForAuth(auth, workspace);
        return jsonResponse(mcpResult(id, {
          tools,
          meta: { workspace, tool_count: tools.length }
        }));
      }

      case 'tools/call':
        if (auth.role === 'none') {
          return jsonResponse(mcpError(id, -32001, 'Unauthorized. Add your Formaut MCP token as the Bearer token in claude.ai → Settings → Integrations.'));
        }
        return handleToolCall(id, params, env, auth, resolveMcpWorkspace(request));

      case 'notifications/initialized':
        return jsonResponse(mcpResult(id, {}));

      default:
        return jsonResponse(mcpError(id, -32601, `Method not found: ${method}`));
    }
  } catch (err) {
    console.error('[MCP] Unhandled error:', err);
    return jsonResponse(mcpError(id, -32603, `Internal error: ${err.message}`));
  }
}

// =============================================================================
// CLIENT TOKEN PROVISIONING
// =============================================================================
// POST /provision-client-token
// Header: Authorization: Bearer <MCP_API_KEY>   (operator only)
// Body:   { "slug": "client-slug" }
// Returns: { token, instructions, claude_ai_setup }
//
// The raw token is returned ONCE and never stored. Only the SHA-256 hash lives
// in the database. If lost, generate a new one — the old hash is overwritten.

async function handleProvisionClientToken(request, env) {
  // Operator auth only
  const authHeader = request.headers.get('Authorization') || '';
  const token = authHeader.startsWith('Bearer ') ? authHeader.slice(7).trim() : '';
  if (!env.MCP_API_KEY || token !== env.MCP_API_KEY) {
    return jsonResponse({ ok: false, error: 'Unauthorized' }, 401);
  }

  let body;
  try { body = await request.json(); } catch {
    return jsonResponse({ ok: false, error: 'Invalid JSON' }, 400);
  }

  const { slug } = body;
  if (!slug) return jsonResponse({ ok: false, error: 'slug is required' }, 400);

  // Verify client exists
  const clientRes = await platformSupabase(env, 'GET',
    `/rest/v1/clients?slug=eq.${enc(slug)}&select=id,slug,display_name&limit=1`
  );
  if (!clientRes.ok) return jsonResponse({ ok: false, error: 'Database error' }, 500);
  const clients = await clientRes.json();
  if (!clients.length) return jsonResponse({ ok: false, error: `Client not found: ${slug}` }, 404);
  const client = clients[0];

  // Generate a cryptographically random token (32 bytes → 64 hex chars)
  const rawToken = Array.from(crypto.getRandomValues(new Uint8Array(32)))
    .map(b => b.toString(16).padStart(2, '0')).join('');

  // Hash and store — raw token is never persisted
  const hash = await sha256hex(rawToken);
  const updateRes = await platformSupabase(env, 'PATCH',
    `/rest/v1/clients?slug=eq.${enc(slug)}`,
    { mcp_token_hash: hash, mcp_token_issued_at: new Date().toISOString() }
  );
  if (!updateRes.ok) return jsonResponse({ ok: false, error: 'Failed to store token hash' }, 500);

  const mcpServerUrl = new URL(request.url).origin + '/mcp';

  return jsonResponse({
    ok:    true,
    slug,
    display_name: client.display_name,
    token: rawToken,
    warning: 'Copy this token now — it will never be shown again. To reset, call this endpoint again.',
    claude_ai_setup: {
      step_1: 'Open claude.ai → Settings → Integrations → Add MCP Server',
      step_2: `URL: ${mcpServerUrl}`,
      step_3: `Bearer token: ${rawToken}`,
      step_4: 'Name it "Formaut" and save.',
      step_5: 'Start a new conversation and paste the Project Instructions below.',
    },
    project_instructions: buildProjectInstructions(client.display_name, slug),
  });
}


async function toolCommitStagedChange({ slug, review_id, decision_note }, env) {
  // Load the review record
  const reviewRes = await platformSupabase(env, 'GET',
    `/rest/v1/artifact_reviews?id=eq.${enc(review_id)}&client_slug=eq.${enc(slug)}&select=id,title,proposed_change,status&limit=1`
  );
  if (!reviewRes.ok) return { ok: false, error: 'Could not fetch review record' };
  const reviews = await reviewRes.json();
  if (!reviews.length) return { ok: false, error: `Review not found: ${review_id}` };
  const review = reviews[0];

  if (review.status !== 'pending') {
    return { ok: false, error: `Review is already ${review.status} — cannot re-commit` };
  }

  let proposed;
  try {
    proposed = typeof review.proposed_change === 'string'
      ? JSON.parse(review.proposed_change)
      : review.proposed_change;
  } catch {
    return { ok: false, error: 'Could not parse proposed_change from review record' };
  }

  const { path, content } = proposed;
  if (!path || !content) return { ok: false, error: 'Review record missing path or content in proposed_change' };

  // Get client credentials
  const client = await getClientCreds(slug, env);
  if (!client.github_token)     return { ok: false, step: 'credentials', error: 'GitHub not connected for this client' };

  // Write file to GitHub
  let fileSha = null;
  const existing = await fetch(
    `https://api.github.com/repos/${client.github_repo}/contents/${path}`,
    { headers: githubHeaders(client.github_token) }
  );
  if (existing.ok) {
    const data = await existing.json();
    fileSha = data.sha;
  }

  const writeRes = await fetch(
    `https://api.github.com/repos/${client.github_repo}/contents/${path}`,
    {
      method: 'PUT',
      headers: { ...githubHeaders(client.github_token), 'Content-Type': 'application/json' },
      body: JSON.stringify({
        message:  `Formaut: ${review.title || 'commit staged change'}`,
        content:  btoa(unescape(encodeURIComponent(content))),
        ...(fileSha ? { sha: fileSha } : {}),
      }),
    }
  );

  if (!writeRes.ok) {
    const err = await writeRes.json();
    return { ok: false, step: 'github_write', error: err.message || 'GitHub write failed' };
  }
  const writeData = await writeRes.json();
  const commitSha = writeData.commit.sha;

  // Mark review as approved and enrich proposed_change with commit provenance (enables rollback)
  const enrichedChange = JSON.stringify({
    ...proposed,
    commit_sha:   commitSha,
    committed_at: new Date().toISOString(),
  });
  await platformSupabase(env, 'PATCH',
    `/rest/v1/artifact_reviews?id=eq.${enc(review_id)}`,
    {
      status:          'approved',
      decided_by:      'mcp_operator',
      decided_at:      new Date().toISOString(),
      approved_at:     new Date().toISOString(),
      decision_note:   decision_note || 'Approved and committed via MCP',
      proposed_change: enrichedChange,
      updated_at:      new Date().toISOString(),
    }
  );

  // Update deploy status on client record
  await platformSupabase(env, 'PATCH',
    `/rest/v1/clients?slug=eq.${enc(slug)}`,
    { last_deploy: new Date().toISOString(), last_deploy_status: 'triggered' }
  );

  // Write session note
  const clientIdRes = await platformSupabase(env, 'GET', `/rest/v1/clients?slug=eq.${enc(slug)}&select=id&limit=1`);
  if (clientIdRes.ok) {
    const idRows = await clientIdRes.json();
    if (idRows.length) {
      await platformSupabase(env, 'POST', '/rest/v1/sessions_index', {
        client_id:    idRows[0].id,
        client_slug:  slug,
        session_date: new Date().toISOString().split('T')[0],
        summary:      `Committed staged change: ${review.title || path}`,
        changes_made: [`Updated ${path} (review ${review_id})`],
        deploy_triggered: true,
        deploy_status: 'triggered',
        signal_count: 0,
        style_signal_count: 0,
      }, { Prefer: 'return=minimal' });
    }
  }

  return {
    ok:         true,
    review_id,
    slug,
    path,
    commit:     commitSha,
    action:     fileSha ? 'updated' : 'created',
    review_status: 'approved',
    deploy:     'triggered — CF Pages will build from this commit',
    poll_with:  'check_deploy_status',
  };
}



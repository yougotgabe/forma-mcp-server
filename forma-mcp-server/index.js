// =============================================================================
// FORMA MCP SERVER
// =============================================================================
// Standalone Cloudflare Worker implementing the Model Context Protocol (MCP).
// Gives Claude in claude.ai direct tool access to the Formaut platform:
//   - Platform Supabase (client records, session history, signals)
//   - Client GitHub repos (read, write, create files)
//   - Cloudflare Pages (deploy, status)
//   - Client Supabase (read/write queries)
//
// Auth: Bearer token in Authorization header — set MCP_API_KEY in Wrangler secrets.
//       This is the key you enter in claude.ai Settings → Integrations → MCP.
//
// Credentials live directly in this Worker (not proxied through platform Worker).
// This is intentional — operator-only tool, clean separation from client-facing infra.
//
// Deploy: wrangler deploy (from forma-mcp-server/ directory)
// Register: claude.ai → Settings → Integrations → Add MCP Server
//   URL: https://forma-mcp-server.<your-subdomain>.workers.dev/mcp
//
// Required Wrangler secrets:
//   MCP_API_KEY                — bearer token for claude.ai auth
//   SUPABASE_URL               — platform Supabase URL
//   SUPABASE_SERVICE_ROLE_KEY  — platform service_role key
//   ENCRYPTION_KEY             — AES-256-GCM key (same as platform Worker)
//   GITHUB_TOKEN               — operator GitHub token
//   CLOUDFLARE_API_TOKEN       — operator CF API token
//   CLOUDFLARE_ACCOUNT_ID      — operator CF account ID
// =============================================================================


async function getClientCreds(slug, env) {
  const res = await platformSupabase(env, 'GET',
    `/rest/v1/clients?slug=eq.${enc(slug)}&select=github_repo,cloudflare_pages_project,cloudflare_account_id,supabase_url,github_token_enc,cloudflare_token_enc,supabase_service_key_enc&limit=1`
  );
  if (!res.ok) throw new Error('Client not found');
  const rows = await res.json();
  if (!rows.length) throw new Error(`Client not found: ${slug}`);
  const client = rows[0];

  // Decrypt on demand
  const creds = { ...client };
  if (client.github_token_enc)         creds.github_token         = await decrypt(client.github_token_enc, env.ENCRYPTION_KEY);
  if (client.cloudflare_token_enc)     creds.cloudflare_token     = await decrypt(client.cloudflare_token_enc, env.ENCRYPTION_KEY);
  if (client.supabase_service_key_enc) creds.supabase_service_key = await decrypt(client.supabase_service_key_enc, env.ENCRYPTION_KEY);

  return creds;
}


// ── Client Supabase ────────────────────────────────────────────────────────

async function toolClientQuery({ slug, sql, table }, env) {
  const client = await getClientCreds(slug, env);
  if (!client.supabase_service_key) return { ok: false, error: 'Supabase not connected for this client' };

  // Convenience: if only table is specified, SELECT *
  const query = sql || `SELECT * FROM ${table} LIMIT 100`;
  const normalised = query.trim().toLowerCase();
  if (!normalised.startsWith('select')) {
    return { ok: false, error: 'client_query only accepts SELECT. Use client_write for mutations.' };
  }

  // Use PostgREST REST API for simple table reads, RPC for complex SQL
  const res = await fetch(`${client.supabase_url}/rest/v1/rpc/exec_sql`, {
    method:  'POST',
    headers: { 'apikey': client.supabase_service_key, 'Content-Type': 'application/json' },
    body:    JSON.stringify({ query }),
  });

  if (res.status === 404 && table) {
    // exec_sql not available — fall back to direct table REST call
    const tableRes = await fetch(`${client.supabase_url}/rest/v1/${table}?select=*&limit=100`, {
      headers: { 'apikey': client.supabase_service_key }
    });
    if (!tableRes.ok) return { ok: false, error: 'Table not found or RLS blocked' };
    const rows = await tableRes.json();
    return { ok: true, rows };
  }

  if (!res.ok) {
    const err = await res.text();
    return { ok: false, error: err };
  }
  const rows = await res.json();
  return { ok: true, rows };
}

async function toolClientWrite({ slug, sql }, env) {
  const client = await getClientCreds(slug, env);
  if (!client.supabase_service_key) return { ok: false, error: 'Supabase not connected' };

  const normalised = sql.trim().toLowerCase();
  if (normalised.startsWith('select')) {
    return { ok: false, error: 'Use client_query for SELECT statements.' };
  }

  const res = await fetch(`${client.supabase_url}/rest/v1/rpc/exec_sql`, {
    method:  'POST',
    headers: { 'apikey': client.supabase_service_key, 'Content-Type': 'application/json' },
    body:    JSON.stringify({ query: sql }),
  });

  if (!res.ok) {
    const err = await res.text();
    return { ok: false, error: err };
  }
  return { ok: true };
}


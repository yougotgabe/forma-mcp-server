async function toolPreviewStagedChange({ slug, review_id }, env) {
  // Load the review record
  const reviewRes = await platformSupabase(env, 'GET',
    `/rest/v1/artifact_reviews?id=eq.${enc(review_id)}&client_slug=eq.${enc(slug)}&select=id,title,status,proposed_change,created_at&limit=1`
  );
  if (!reviewRes.ok) return { ok: false, error: 'Could not fetch review record' };
  const reviews = await reviewRes.json();
  if (!reviews.length) return { ok: false, error: `Review not found: ${review_id}` };
  const review = reviews[0];

  let proposed;
  try {
    proposed = typeof review.proposed_change === 'string'
      ? JSON.parse(review.proposed_change)
      : review.proposed_change;
  } catch {
    return { ok: false, error: 'Could not parse proposed_change' };
  }

  const { path, content: proposedContent, previous_content, risk_level } = proposed;
  if (!path || !proposedContent) return { ok: false, error: 'Review missing path or content' };

  // Determine before content: use stored previous_content, or fetch from GitHub
  let beforeContent = previous_content || null;
  if (!beforeContent) {
    try {
      const client = await getClientCreds(slug, env);
      if (client.github_token && client.github_repo) {
        const ghRes = await fetch(
          `https://api.github.com/repos/${client.github_repo}/contents/${path}`,
          { headers: githubHeaders(client.github_token) }
        );
        if (ghRes.ok) {
          const ghData = await ghRes.json();
          beforeContent = ghData.content
            ? decodeURIComponent(escape(atob(ghData.content.replace(/\n/g, ''))))
            : null;
        }
      }
    } catch { /* no GitHub — diff against empty */ }
  }

  // Compute diff
  const beforeLines  = (beforeContent || '').split('\n');
  const afterLines   = proposedContent.split('\n');
  const maxLen       = Math.max(beforeLines.length, afterLines.length);
  const changedLines = [];
  let added = 0, removed = 0, changed = 0;
  for (let i = 0; i < maxLen; i++) {
    const b = beforeLines[i] ?? null;
    const a = afterLines[i]  ?? null;
    if (b !== a) {
      if (b === null) added++;
      else if (a === null) removed++;
      else changed++;
      if (changedLines.length < 12) changedLines.push({ line: i + 1, before: b, after: a });
    }
  }
  const lineDelta = afterLines.length - beforeLines.length;

  // Safety checks (HTML-specific)
  const isHtml = path.endsWith('.html') || path.endsWith('.htm');
  const safetyChecks = [];
  if (isHtml) {
    safetyChecks.push({ check: 'has_title',    ok: /<title>/i.test(proposedContent) });
    safetyChecks.push({ check: 'has_viewport', ok: /name=.viewport/i.test(proposedContent) });
    safetyChecks.push({ check: 'no_inline_scripts', ok: !/<script(?![^>]*src)[^>]*>/i.test(proposedContent) });
    safetyChecks.push({ check: 'no_iframes',   ok: !/<iframe/i.test(proposedContent) });
  }
  const allSafetyPass = safetyChecks.every(c => c.ok);

  // Approval recommendation
  const riskOk   = !['high', 'dangerous', 'review_required'].includes(risk_level);
  const smallChange = (changed + added + removed) < 50;
  const recommend_approval = allSafetyPass && (riskOk || smallChange);

  return {
    ok:          true,
    review_id,
    slug,
    path,
    status:      review.status,
    title:       review.title,
    risk_level:  risk_level || 'unknown',
    diff: {
      line_delta:   lineDelta,
      lines_added:  added,
      lines_removed: removed,
      lines_changed: changed,
      sample:       changedLines,
    },
    safety: {
      checks:    safetyChecks,
      all_pass:  allSafetyPass,
    },
    recommend_approval,
    note: recommend_approval
      ? 'Change looks safe. Call commit_staged_change to deploy.'
      : `Review carefully before committing — risk_level: ${risk_level}, safety issues: ${safetyChecks.filter(c => !c.ok).map(c => c.check).join(', ') || 'none'}.`,
  };
}

async function toolRollbackDeployment({ slug, review_id, path: explicitPath, previous_content: explicitContent, reason }, env) {
  let restorePath    = explicitPath    || null;
  let restoreContent = explicitContent || null;
  let sourceReviewId = review_id       || null;

  // If review_id supplied, pull previous_content from the record
  if (review_id && (!restorePath || !restoreContent)) {
    const reviewRes = await platformSupabase(env, 'GET',
      `/rest/v1/artifact_reviews?id=eq.${enc(review_id)}&client_slug=eq.${enc(slug)}&select=id,proposed_change,status&limit=1`
    );
    if (!reviewRes.ok) return { ok: false, error: 'Could not fetch review record' };
    const reviews = await reviewRes.json();
    if (!reviews.length) return { ok: false, error: `Review not found: ${review_id}` };
    const review = reviews[0];
    let proposed;
    try {
      proposed = typeof review.proposed_change === 'string'
        ? JSON.parse(review.proposed_change)
        : review.proposed_change;
    } catch {
      return { ok: false, error: 'Could not parse proposed_change from review' };
    }
    if (!proposed.previous_content) {
      return {
        ok: false,
        error: 'Review does not contain previous_content — it was staged before rollback capture was added, or the file did not exist at staging time. Supply path and previous_content explicitly to force a rollback.',
      };
    }
    restorePath    = restorePath    || proposed.path;
    restoreContent = restoreContent || proposed.previous_content;
  }

  if (!restorePath || !restoreContent) {
    return { ok: false, error: 'Must supply either review_id (with captured previous_content) or explicit path + previous_content.' };
  }

  // Get client credentials
  const client = await getClientCreds(slug, env);
  if (!client.github_token) return { ok: false, error: 'GitHub not connected for this client' };

  // Get current file SHA (required for GitHub update)
  let currentSha = null;
  const currentRes = await fetch(
    `https://api.github.com/repos/${client.github_repo}/contents/${restorePath}`,
    { headers: githubHeaders(client.github_token) }
  );
  if (currentRes.ok) {
    const d = await currentRes.json();
    currentSha = d.sha || null;
  }

  // Write restoration to GitHub
  const writeRes = await fetch(
    `https://api.github.com/repos/${client.github_repo}/contents/${restorePath}`,
    {
      method: 'PUT',
      headers: { ...githubHeaders(client.github_token), 'Content-Type': 'application/json' },
      body: JSON.stringify({
        message:  `Formaut rollback: ${restorePath}${reason ? ` — ${reason}` : ''}`,
        content:  btoa(unescape(encodeURIComponent(restoreContent))),
        ...(currentSha ? { sha: currentSha } : {}),
      }),
    }
  );

  if (!writeRes.ok) {
    const err = await writeRes.json();
    return { ok: false, step: 'github_write', error: err.message || 'GitHub write failed' };
  }
  const writeData = await writeRes.json();
  const commitSha = writeData.commit.sha;

  // Update deploy status
  await platformSupabase(env, 'PATCH',
    `/rest/v1/clients?slug=eq.${enc(slug)}`,
    { last_deploy: new Date().toISOString(), last_deploy_status: 'triggered' }
  );

  // Mark source review as rolled_back
  if (sourceReviewId) {
    await platformSupabase(env, 'PATCH',
      `/rest/v1/artifact_reviews?id=eq.${enc(sourceReviewId)}`,
      { status: 'rolled_back', updated_at: new Date().toISOString() }
    );
  }

  // Write session note
  const clientIdRes = await platformSupabase(env, 'GET', `/rest/v1/clients?slug=eq.${enc(slug)}&select=id&limit=1`);
  if (clientIdRes.ok) {
    const idRows = await clientIdRes.json();
    if (idRows.length) {
      await platformSupabase(env, 'POST', '/rest/v1/sessions_index', {
        client_id:        idRows[0].id,
        client_slug:      slug,
        session_date:     new Date().toISOString().split('T')[0],
        summary:          `Rollback: ${restorePath}${reason ? ` — ${reason}` : ''}`,
        changes_made:     [`Rolled back ${restorePath} to previous version (commit ${commitSha})`],
        deploy_triggered: true,
        deploy_status:    'triggered',
        signal_count:     0,
        style_signal_count: 0,
      }, { Prefer: 'return=minimal' });
    }
  }

  return {
    ok:            true,
    slug,
    path:          restorePath,
    commit:        commitSha,
    deploy:        'triggered — CF Pages will build from this commit',
    source_review: sourceReviewId || null,
    review_status: sourceReviewId ? 'rolled_back' : null,
    poll_with:     'check_deploy_status',
  };
}


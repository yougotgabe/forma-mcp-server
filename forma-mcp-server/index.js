async function toolEncryptCredential({ slug, field, plaintext }, env) {
  const ALLOWED = new Set(['github_token_enc','cloudflare_token_enc','supabase_mgmt_token_enc','supabase_service_key_enc','supabase_anon_key_enc','printify_key_enc']);
  if (!ALLOWED.has(field)) return { ok: false, error: `Field '${field}' is not a valid credential field` };
  if (!plaintext || !plaintext.trim()) return { ok: false, error: 'plaintext is required' };

  // Encrypt immediately — plaintext exists in memory only for this operation
  let ciphertext;
  try {
    ciphertext = await encrypt(plaintext, env.ENCRYPTION_KEY);
  } catch (err) {
    return { ok: false, error: 'Encryption failed: ' + err.message };
  }

  // Write encrypted value to clients table
  const res = await platformSupabase(env, 'PATCH',
    `/rest/v1/clients?slug=eq.${enc(slug)}`,
    { [field]: ciphertext }
  );

  if (!res.ok) {
    const err = await res.text();
    return { ok: false, error: err };
  }

  // plaintext is no longer referenced after this point
  return { ok: true, slug, field, message: 'Credential encrypted and stored. Plaintext discarded.' };
}

// AES-256-GCM encryption (matches platform Worker exactly)
async function encrypt(plaintext, hexKey) {
  const keyBytes = hexToBytes(hexKey);
  const key = await crypto.subtle.importKey(
    'raw', keyBytes, { name: 'AES-GCM' }, false, ['encrypt']
  );
  const iv = crypto.getRandomValues(new Uint8Array(12));
  const encoded = new TextEncoder().encode(plaintext);
  const cipherBuf = await crypto.subtle.encrypt({ name: 'AES-GCM', iv }, key, encoded);
  const combined = new Uint8Array(iv.byteLength + cipherBuf.byteLength);
  combined.set(iv, 0);
  combined.set(new Uint8Array(cipherBuf), iv.byteLength);
  return btoa(String.fromCharCode(...combined));
}


async function toolDeployChange({ slug, path, content, commit_message }, env) {
  // 1. Write file to GitHub
  const client = await getClientCreds(slug, env);
  if (!client.github_token)     return { ok: false, error: 'GitHub not connected' };
  if (!client.cloudflare_token) return { ok: false, error: 'Cloudflare not connected' };

  // Get existing SHA if file exists (needed for update vs create)
  let fileSha = null;
  const existing = await fetch(
    `https://api.github.com/repos/${client.github_repo}/contents/${path}`,
    { headers: githubHeaders(client.github_token) }
  );
  if (existing.ok) {
    const data = await existing.json();
    fileSha = data.sha;
  }

  const writeRes = await fetch(
    `https://api.github.com/repos/${client.github_repo}/contents/${path}`,
    {
      method: 'PUT',
      headers: { ...githubHeaders(client.github_token), 'Content-Type': 'application/json' },
      body: JSON.stringify({
        message:  commit_message || `Formaut: update ${path}`,
        content:  btoa(unescape(encodeURIComponent(content))),
        ...(fileSha ? { sha: fileSha } : {}),
      }),
    }
  );
  if (!writeRes.ok) {
    const err = await writeRes.json();
    return { ok: false, step: 'write_file', error: err.message || 'GitHub write failed' };
  }
  const writeData = await writeRes.json();
  const commitSha = writeData.commit.sha;

  // 2. Trigger deploy — empty commit on top so CF Pages picks up the change
  //    (the file write above already triggers it via push to main, but we record it)
  await platformSupabase(env, 'PATCH',
    `/rest/v1/clients?slug=eq.${enc(slug)}`,
    { last_deploy: new Date().toISOString(), last_deploy_status: 'triggered' }
  );

  return {
    ok:            true,
    path,
    action:        fileSha ? 'updated' : 'created',
    commit:        commitSha,
    deploy:        'triggered — push to main starts CF Pages build automatically',
    poll_with:     'check_deploy_status',
    note:          'File written and deploy triggered in one step.',
  };
}


function validateEmailHtml({ html, text, channel, subject, purpose }) {
  const warnings = [];
  const lower = String(html || '').toLowerCase();
  if (!html || html.trim().length < 40) warnings.push('HTML is missing or too short.');
  if (!subject) warnings.push('Subject is required.');
  if (!purpose) warnings.push('Purpose is required.');
  if (!text) warnings.push('Plain-text fallback is missing.');
  if (/<script[\s>]/i.test(html)) warnings.push('Email HTML must not contain <script>.');
  if (/<iframe[\s>]/i.test(html)) warnings.push('Email HTML must not contain <iframe>.');
  if (/<form[\s>]/i.test(html)) warnings.push('Email HTML must not contain <form>.');
  if (/type=["']module["']|\.js["']|javascript:/i.test(html)) warnings.push('Email HTML must not depend on JavaScript.');
  if (/<nav[\s>]|<section[\s>].*<section[\s>]|position:\s*fixed|min-height:\s*100vh/i.test(lower)) warnings.push('HTML looks more like a webpage than a compact email template.');
  if (channel === 'campaign') {
    if (!/unsubscribe/i.test(html + ' ' + text)) warnings.push('Campaign emails should include unsubscribe language/link.');
    if (!/address|sent by|business/i.test(html + ' ' + text)) warnings.push('Campaign emails should include business identity/footer language.');
  }
  const hard = warnings.some(w => /must not|missing or too short|required/.test(w));
  const webpageSignals = warnings.filter(w => /webpage/.test(w)).length;
  const confidence = hard ? 'low' : webpageSignals ? 'medium' : 'high';
  const risk = hard ? 'medium' : channel === 'campaign' ? 'medium' : 'low';
  return {
    email_safe: !hard,
    email_type_confidence: confidence,
    risk_level: risk,
    warnings,
  };
}

function classifyEmailRuleRisk({ rule_type, trigger_type, recipient_config = {}, provider, linked_email_artifact_id }) {
  const warnings = [];
  let risk = 'low';
  if (!recipient_config || typeof recipient_config !== 'object') warnings.push('recipient_config must be a JSON object.');
  if (rule_type === 'campaign') {
    risk = 'medium';
    if (!linked_email_artifact_id) warnings.push('Campaign rules should link to an approved email artifact.');
  }
  if (provider === 'manual') warnings.push('Provider is manual; rule can be tracked but not automatically executed.');
  if (/payment|password|credential|token|auth|billing/i.test(trigger_type || '')) risk = 'high';
  if (recipient_config?.send_to_all_customers || recipient_config?.audience === 'all') risk = risk === 'high' ? 'high' : 'medium';
  return { risk_level: risk, warnings };
}

function buildEmailAttention({ artifacts, rules, events, client }) {
  const attention = [];
  if (!client.github_token) attention.push('GitHub is not connected, so new email HTML artifacts cannot be stored safely.');
  if (!client.supabase_service_key) attention.push('Supabase is not connected, so email registry data cannot be read or written.');
  const unsafe = artifacts.filter(a => !a.email_safe);
  if (unsafe.length) attention.push(`${unsafe.length} email artifact(s) have validation warnings and should not be activated yet.`);
  const pending = rules.filter(r => ['draft', 'pending_review'].includes(r.status));
  if (pending.length) attention.push(`${pending.length} email rule(s) are waiting for review or activation.`);
  const failed = events.filter(e => ['failed', 'bounced', 'suppressed'].includes(e.status));
  if (failed.length) attention.push(`${failed.length} recent email event(s) failed, bounced, or were suppressed.`);
  const resendRules = rules.filter(r => r.provider === 'resend');
  if (resendRules.length && !rules.some(r => r.provider === 'resend' && r.status === 'active')) attention.push('Resend rules exist, but no active Resend rule is running.');
  if (!attention.length) attention.push('Email operations look healthy.');
  return attention;
}

